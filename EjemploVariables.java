
// Una variable es un espacio en la memoria del computador que tiene un nombre y guarda un valor, 
// el cual puede cambiar mientras el programa se ejecuta.

public class EjemploVariables {
    public static void main(String[] args) {
        int numero = 10;               // almacena un número entero
        double precio = 99.99;         // almacena un número decimal
        String saludo = "Hola mundo";  // almacena texto
        boolean encendido = false;     // almacena verdadero o falso
        int numero2= 15;
        int suma= numero+numero2;

        System.out.println(numero);
        System.out.println(precio);
        System.out.println(saludo);
        System.out.println(encendido);
        System.out.println(numero2);
        System.out.println("La suma es : "+suma);
    }
}

